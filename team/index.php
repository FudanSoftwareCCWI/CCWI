							<h3>关于我们</h3>
							<p>我们授课团队的成员来自但不限于复旦大学、上海交通大学优秀的大学生。另外我们还得到了北京大学，清华大学以及UC Berkeley等兄弟高校朋友们的数据信息技术支持。每一个成员不仅在自主招生各项招生计划中均有所斩获，且高考成绩也都在各省中位于前列。</p>
							<p>大学生涯对于一个人的未来至关重要，然而大学专业选择更是关乎未来甚至一生的发展方向。我们目睹了许许多多因为各种原因错误选择专业而耽误了整个大学时光的案例。毋庸置疑，在中国，专业选择的重要性与专业知识的普及被严重的低估了。而我们就是要填补市场上的这一空白。</p>
							<p>“初识大学”授课团队的专业领域涵盖了大部分中国教育部划分的专业。同时我们的核心授课团队成员均排名专业前10%。 7位教学组长中有5位获得过中国大学生最高奖学荣誉——国家奖学金。在我们的创始团队的简历上还有——NOIP一等奖，各类奖学金，校优秀学生标兵，学生会优秀骨干，各类社会实践项目等等荣誉………… </p>
							<p>然而这些都不是重点，“初识大学”的创始团队成员中有一半以上拥有转专业的经历，我们对于专业的选择有着更深刻的领悟与跨专业的独特见解。我们相信，过来人的经验教训会成为后继者前行闪亮的灯塔。</p>
							<p>我们希望能够通过自身的经验，为高中生普及专业知识，帮助专业选择，明确人生规划。我们还相信，通过我们微博的努力————在未来，孩子们除了关心如何进入大学之外，会更加关心在大学校园的自己究竟要学些什么，未来的自己究竟要做些什么。</p>
							<p>机遇，永远是留给提前做好准备的人！</p>
